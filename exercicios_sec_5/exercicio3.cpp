/*3. Faça um algoritmo utilizando o comando while que mostra uma contagem regressiva
na tela, iniciando em 10 e terminando em O. Mostrar uma mensagem “FIM!' após a
contagem.*/

#include<stdio.h>


main(){
	
	int n=11;
	
	while(n!=0){
		n=n-1;
		printf("%d\n",n);
		
	}printf("\nFIM");
	
	return 0;
}
